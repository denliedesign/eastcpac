            <p>
                Dance is a very physical activity that requires a lot of jumping, which can put stress on bones and joints.  Most dance footwear does not provide any cushioning or support, so the shock of dance movements can place a lot of pressure on the knees and back.  The best way to prevent potential injury is by choosing a studio with either a professional hardwood or "floating" floor. A floating floor is a dance floor that rests on a system of high-density foam to absorb the shock of jumping.  A high-density foam base is superior to a "sprung" floor, which usually consists of a wood structure built on the regular floor.  The top layer of the floating floor is also an important factor.  A vinyl composite "marley" floor is accepted worldwide as the ideal surface layer for recreational and professional dance.  Companies such as the Royal Winnipeg Ballet and Riverdance – The Touring Show use marley floors as their dance surface.  A marley floor is not slippery, but still allows dancers to slide with a degree of "controlled slip."  With this type of floor, there is less risk of slips and falls.  Very few studios use professional marley floors because of the expense involved, and usually opt for a regular floor tile or laminate for a studio floor. A professional hardwood floor is a one-of-a-kind dance surface due to the huge expense involved in building a wooden dance floor.  Like a floating floor, our hardwood floors are designed to give way as downward pressure is exerted, thus reducing fatigue and helping prevent injuries. All of our studios have professional hardwood or "floating" floors that cover over 4,100 square feet of classroom space.  Our special floors help reduce the risk of injuries and allow students to dance longer without getting tired. "You've got to look the part to play the role." Many famous New York City dance studios and ballet facilities are spread over one or more upper levels of a city building.  They have huge picture windows and large da